double bm = 1.3;
double bc = 10;
double dm[8];
double dc[8];
double e[8];
double step = 0.01;
int minimum_found = 0;
double best_error = 999999999;
double om[] = {0,1,1, 1, 0,-1,-1,-1};
double oc[] = {1,1,0,-1,-1,-1, 0, 1};
int best_error_i;
double be;

double residual_error(double x, double y, double m, double c) {
  double e = (m * x) + c - y;
  return e * e;
}

double rms_error(double m, double c) {
  int i;
  double mean;
  double error_sum = 0;
  
  for(i=0; i<n_data; i++) {
 
    //error_sum += residual_error(data[i].x, data[i].y, m, c); 
   //printf("%0.2lf,%0.2lf\n",data[i].x, data[i].y);
  }

  mean = error_sum / n_data;
  
  return sqrt(mean);
}

void *rms_thread(void *args){
  long i;
  i = (long)args;

  dm[i] = bm + (om[i] * step);
  dc[i] = bc + (oc[i] * step); 

  e[i] = rms_error(dm[i], dc[i]);
  if(e[i] < best_error) {
   best_error = e[i];
   best_error_i = i;
  }
 pthread_exit(NULL);
}

//function to calculate running time of crack function
int time_difference(struct timespec *start, struct timespec *finish,
                    long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec; 
  long long int dn =  finish->tv_nsec - start->tv_nsec; 

  if(dn < 0 ) {
    ds--;
    dn += 1000000000; 
  } 
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}
int main() {
  long i;
  pthread_t *t = malloc(sizeof(pthread_t) * n_threads);
  struct timespec start, finish;   
  long long int time_elapsed;
  
  be = rms_error(bm, bc);
  clock_gettime(CLOCK_MONOTONIC, &start);
  while(!minimum_found) { 
     for(i=0;i<n_threads;i++) {
       pthread_create(&t[i], NULL, rms_thread,(void *)i);
       pthread_join(t[i], NULL);
     }

    //printf("best m,c is %lf,%lf with error %lf in direction %d\n", 
    //dm[best_error_i], dc[best_error_i], best_error, best_error_i);
    if(best_error < be) {
       be = best_error;
       bm = dm[best_error_i];
       bc = dc[best_error_i];
     } else {
      minimum_found = 1;
    }
  }
  clock_gettime(CLOCK_MONOTONIC, &finish);
  printf("minimum m,c is %lf,%lf with error %lf\n", bm, bc, be);
  time_difference(&start, &finish, &time_elapsed);
  printf("Elapsed time %9.5lfs\n", time_elapsed/1000000000.0);


  

  free(t);
  pthread_exit(NULL);
}

